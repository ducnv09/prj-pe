package model;

import java.util.Date;

/**
 *
 * @author Asus
 */
public class Train {
    private int code;
    private String name;
    private Date time;

    public Train() {
    }

    public Train(int code, String name, Date time) {
        this.code = code;
        this.name = name;
        this.time = time;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }
    
    
}
